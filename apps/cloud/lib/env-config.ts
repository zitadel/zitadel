"use server"

import { readFile, writeFile } from "fs/promises"
import { existsSync } from "fs"
import path from "path"
import { serviceConfigs } from "./service-configs"

/**
 * Server action that reads and writes the cloud app's .env.local file.
 * Only available in development mode.
 */

const ENV_FILE = path.join(process.cwd(), ".env.local")

export interface EnvConfig {
  [key: string]: string | undefined
}

function parseEnvFile(content: string): EnvConfig {
  const config: EnvConfig = {}
  for (const line of content.split("\n")) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith("#")) continue
    const eqIdx = trimmed.indexOf("=")
    if (eqIdx === -1) continue
    const key = trimmed.slice(0, eqIdx).trim()
    let value = trimmed.slice(eqIdx + 1).trim()
    if ((value.startsWith('"') && value.endsWith('"')) ||
        (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1)
    }
    if (value) config[key] = value
  }
  return config
}

function serializeEnvFile(config: EnvConfig): string {
  const lines: string[] = [
    "# Generated by ZITADEL Cloud debug page",
    "# Restart the dev server after changes",
    "",
  ]
  
  for (const section of serviceConfigs) {
    const sectionVars = section.vars.filter(v => config[v.key])
    if (sectionVars.length > 0) {
      lines.push(`# ${section.title}`)
      for (const v of sectionVars) {
        lines.push(`${v.key}=${config[v.key]}`)
      }
      lines.push("")
    }
  }
  
  const knownKeys = new Set(serviceConfigs.flatMap(s => s.vars.map(v => v.key)))
  const additionalKeys = Object.keys(config).filter(k => !knownKeys.has(k) && config[k])
  if (additionalKeys.length > 0) {
    lines.push("# Additional")
    for (const key of additionalKeys) {
      lines.push(`${key}=${config[key]}`)
    }
    lines.push("")
  }
  
  return lines.join("\n")
}

export async function readEnvConfig(): Promise<{ config: EnvConfig; exists: boolean }> {
  if (process.env.NODE_ENV !== "development") {
    return { config: {}, exists: false }
  }
  if (!existsSync(ENV_FILE)) {
    return { config: {}, exists: false }
  }
  try {
    const raw = await readFile(ENV_FILE, "utf-8")
    return { config: parseEnvFile(raw), exists: true }
  } catch {
    return { config: {}, exists: false }
  }
}

export async function writeEnvConfig(config: EnvConfig): Promise<{ success: boolean; error?: string }> {
  if (process.env.NODE_ENV !== "development") {
    return { success: false, error: "Only available in development mode" }
  }
  try {
    const content = serializeEnvFile(config)
    await writeFile(ENV_FILE, content, "utf-8")
    return { success: true }
  } catch (e) {
    return { success: false, error: String(e) }
  }
}
